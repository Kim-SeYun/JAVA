package scanner_exam;

public class AssignmentOperatorExample {
	public static void main(String[] args) {
		int result = 5;
		result += 10;
		// result = result + 10
		System.out.println(result);
		
		result = 10;
		result -= 3;
		System.out.println(result);
		
		result *= 3;
		System.out.println(result);
		
		result /= 3;
		System.out.println(result);
		
		result %= 2;
		System.out.println(result);
	}

}
